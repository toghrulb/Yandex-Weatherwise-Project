from __future__ import annotations

import json
from pathlib import Path

import pandas as pd

from ml.src.data.load_data import load_hourly_data
from ml.src.data.split_manager import SplitConfig, ensure_split_assignments
from ml.src.evaluation.evaluate_all_test import evaluate_all_test
from ml.src.models.train_pipeline import run_full_training
from ml.src.utils.paths import PROJECT_ROOT


def _scenario_configs() -> list[tuple[str, SplitConfig]]:
    return [
        (
            "future_only_last_10d_val_10d_test",
            SplitConfig(
                strategy="chronological_by_date_windows",
                val_days=10,
                test_days=10,
            ),
        ),
        (
            "monthly_70_15_15",
            SplitConfig(
                strategy="monthly_chronological_ratio",
                monthly_train_ratio=0.70,
                monthly_val_ratio=0.15,
                monthly_test_ratio=0.15,
            ),
        ),
    ]


def _split_profile(df: pd.DataFrame, assignments: pd.DataFrame) -> dict:
    merged = df.merge(assignments[["obs_id", "split"]], on="obs_id", how="inner", validate="1:1")
    season_mix = pd.crosstab(merged["split"], merged["season"], normalize="index") * 100.0
    counts = merged["split"].value_counts().to_dict()

    def _bounds(split_name: str) -> tuple[str, str]:
        part = assignments[assignments["split"] == split_name]["timestamp"]
        return str(part.min()), str(part.max())

    train_start, train_end = _bounds("train")
    val_start, val_end = _bounds("val")
    test_start, test_end = _bounds("test")

    return {
        "counts": {k: int(v) for k, v in counts.items()},
        "boundaries": {
            "train": [train_start, train_end],
            "val": [val_start, val_end],
            "test": [test_start, test_end],
        },
        "season_mix_pct": {
            split: {season: float(round(val, 2)) for season, val in row.items()}
            for split, row in season_mix.to_dict(orient="index").items()
        },
    }


def _write_checkpoint_report(results: dict, output_path: Path) -> None:
    lines = [
        "# Split Strategy Comparison",
        "",
        "Generated by `python -m ml.src.evaluation.compare_split_strategies`.",
        "",
        "| Scenario | Umbrella F1 (test) | Clothing Acc (test) | Clothing Macro F1 (test) | Suitability MAE (test) | Suitability R2 (test) |",
        "|---|---:|---:|---:|---:|---:|",
    ]
    for name, res in results.items():
        t = res["test_metrics"]
        lines.append(
            f"| {name} | {t['umbrella']['f1']:.4f} | {t['clothing']['accuracy']:.4f} | "
            f"{t['clothing']['macro_f1']:.4f} | {t['suitability']['mae']:.4f} | {t['suitability']['r2']:.4f} |"
        )

    lines.extend(["", "## Scenario Details", ""])

    for name, res in results.items():
        lines.extend(
            [
                f"### {name}",
                "",
                f"- Strategy: `{res['split_config']['strategy']}`",
                f"- Split counts: `{res['split_profile']['counts']}`",
                f"- Train window: `{res['split_profile']['boundaries']['train'][0]}` -> `{res['split_profile']['boundaries']['train'][1]}`",
                f"- Val window: `{res['split_profile']['boundaries']['val'][0]}` -> `{res['split_profile']['boundaries']['val'][1]}`",
                f"- Test window: `{res['split_profile']['boundaries']['test'][0]}` -> `{res['split_profile']['boundaries']['test'][1]}`",
                f"- Season mix (%): `{res['split_profile']['season_mix_pct']}`",
                f"- Validation metrics: `{res['validation_metrics']}`",
                f"- Test metrics: `{res['test_metrics']}`",
                "",
            ]
        )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines), encoding="utf-8")


def compare_split_strategies() -> dict:
    df = load_hourly_data()
    df = df.dropna(subset=["obs_id", "timestamp"]).copy()
    df["obs_id"] = df["obs_id"].astype(str)

    comparison: dict[str, dict] = {}
    for scenario_name, cfg in _scenario_configs():
        assignments = ensure_split_assignments(df=df, cfg=cfg, force_rebuild=True)
        validation_metrics = run_full_training(
            force_rebuild_split=False,
            split_config=cfg,
            write_report=False,
        )
        test_metrics = evaluate_all_test(
            split_config=cfg,
            write_reports=False,
        )
        comparison[scenario_name] = {
            "split_config": {
                "strategy": cfg.strategy,
                "val_days": cfg.val_days,
                "test_days": cfg.test_days,
                "monthly_train_ratio": cfg.monthly_train_ratio,
                "monthly_val_ratio": cfg.monthly_val_ratio,
                "monthly_test_ratio": cfg.monthly_test_ratio,
            },
            "split_profile": _split_profile(df=df, assignments=assignments),
            "validation_metrics": validation_metrics,
            "test_metrics": test_metrics,
        }

    _write_checkpoint_report(
        results=comparison,
        output_path=PROJECT_ROOT / "docs" / "checkpoint_split_comparison.md",
    )
    return comparison


if __name__ == "__main__":
    output = compare_split_strategies()
    print("Split strategy comparison complete.")
    print(json.dumps(output, indent=2))

